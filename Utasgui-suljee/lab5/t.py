data = [
    0x7B, 0xBD, 0x70, 0xF6, 0x7C, 0x1B, 0x59, 0x03,
    0x79, 0x9C, 0x52, 0xF5, 0xCB, 0x8E, 0x54, 0xE6,
    0x84, 0xF7, 0x23, 0x68, 0xE6, 0x08, 0x6A, 0x62,
    0x6D, 0x31, 0xD8, 0x1A, 0x10, 0x69, 0xC1, 0x53,
    0x21, 0x0E, 0x4E, 0x6F, 0x59, 0x4D, 0x2C, 0x08,
    0xE3, 0x7F, 0xFD, 0x6B, 0xC8, 0x06, 0x3E, 0x32,
    0x49, 0xD1, 0x20, 0x90
]

flag_bytes = []

for i, b in enumerate(data):
    # This formula comes from analyzing fcn.000012c9 assembly:
    # The index-based transformation uses this pattern (from the imul/shr/xor sequence)
    x = ((i * 0x55) & 0xFF)  # simplified version of index math
    decoded_byte = b ^ x
    flag_bytes.append(decoded_byte)

flag = ''.join(chr(b) for b in flag_bytes)
print("Flag:", "HZ2025{" + flag + "}")
